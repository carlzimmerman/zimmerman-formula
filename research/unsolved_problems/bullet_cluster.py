from scipy import integrate

def lookback_time(z):
    """Lookback time in Gyr"""
    def integrand(zp):
        return 1 / ((1 + zp) * E_z(zp))
    result, _ = integrate.quad(integrand, 0, z)
    H0_SI = H0 * 1000 / Mpc
    t_H = 1 / H0_SI / (3.154e16)  # Hubble time in Gyr
    return result * t_H

for z in [0, 0.1, 0.2, 0.296, 0.5, 1.0]:
    t_lb = lookback_time(z)
