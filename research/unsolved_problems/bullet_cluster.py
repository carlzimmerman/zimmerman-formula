# Calculate escape velocity enhancement
def escape_velocity_mond(M_solar, R_kpc, a0):
    """Escape velocity in MOND"""
    M_kg = M_solar * 1.989e30
    R_m = R_kpc * kpc
    g_N = G * M_kg / R_m**2

    if g_N < a0:
        # Deep MOND
        v_esc = (G * M_kg * a0)**0.25 * np.sqrt(2)
    else:
        # Newtonian
        v_esc = np.sqrt(2 * G * M_kg / R_m)

    return v_esc / 1000  # km/s

