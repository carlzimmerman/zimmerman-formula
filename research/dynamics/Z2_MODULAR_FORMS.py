# Index calculations
def gamma_index(N):
    """Compute [SL(2,ℤ) : Γ(N)]."""
    if N == 1:
        return 1
    # Formula: N³ ∏_{p|N} (1 - 1/p²)
    result = N**3
    for p in range(2, N+1):
        if N % p == 0 and all(p % i != 0 for i in range(2, p)):
            result = result * (1 - 1/p**2)
    return int(result)

