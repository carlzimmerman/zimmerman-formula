# Construct S_3 explicitly
def construct_S3():
    """Construct the symmetric group S₃ as permutation matrices."""
    elements = list(permutations([0, 1, 2]))
    matrices = []
    names = []

    cycle_type = {
        (0,1,2): "e (identity)",
        (0,2,1): "(23) transposition",
        (1,0,2): "(12) transposition",
        (1,2,0): "(123) 3-cycle",
        (2,0,1): "(132) 3-cycle",
        (2,1,0): "(13) transposition"
    }

    for perm in elements:
        matrix = np.zeros((3, 3))
        for i, j in enumerate(perm):
            matrix[i, j] = 1
        matrices.append(matrix)
        names.append(cycle_type[perm])

    return matrices, names

