class SimpleStructureEstimator:
    """
    Fallback structure estimator when ESMFold is not available.
    Uses secondary structure prediction and geometric heuristics.
    """

    def __init__(self):
        self.initialized = True

    def initialize(self):
        return True

    def predict(self, sequence: str, name: str = "protein") -> Optional[Dict]:
        """Generate approximate structure using secondary structure prediction."""

        # Simple secondary structure propensities (Chou-Fasman)
        helix_formers = set("AELM")
        sheet_formers = set("VIY")

        # Predict secondary structure
        ss_prediction = []
        for i, aa in enumerate(sequence):
            if aa in helix_formers:
                ss_prediction.append('H')
            elif aa in sheet_formers:
                ss_prediction.append('E')
            else:
                ss_prediction.append('C')

        # Generate approximate PDB
        pdb_lines = self._generate_approximate_pdb(sequence, ss_prediction, name)

        # Estimate confidence (lower for this simple method)
        estimated_plddt = [50.0 + (10 if ss != 'C' else 0) for ss in ss_prediction]

        return {
            "name": name,
            "sequence_length": len(sequence),
            "pdb_string": '\n'.join(pdb_lines),
            "plddt": estimated_plddt,
            "mean_plddt": round(sum(estimated_plddt) / len(estimated_plddt), 2),
            "prediction_time": datetime.now().isoformat(),
            "method": "simple_estimation",
            "warning": "Approximate structure - ESMFold not available"
        }

    def _generate_approximate_pdb(self, sequence: str, ss: List[str], name: str) -> List[str]:
        """Generate approximate PDB coordinates."""
        import math

        lines = [
            f"HEADER    APPROXIMATE STRUCTURE",
            f"TITLE     {name}",
            f"REMARK    Generated by M4 Simple Structure Estimator",
            f"REMARK    WARNING: This is an approximation, not a true prediction"
        ]

        # Amino acid 3-letter codes
        aa_codes = {
            'A': 'ALA', 'C': 'CYS', 'D': 'ASP', 'E': 'GLU', 'F': 'PHE',
            'G': 'GLY', 'H': 'HIS', 'I': 'ILE', 'K': 'LYS', 'L': 'LEU',
            'M': 'MET', 'N': 'ASN', 'P': 'PRO', 'Q': 'GLN', 'R': 'ARG',
            'S': 'SER', 'T': 'THR', 'V': 'VAL', 'W': 'TRP', 'Y': 'TYR'
        }

        # Generate backbone trace
        x, y, z = 0.0, 0.0, 0.0
        phi = 0.0

        for i, (aa, ss_type) in enumerate(zip(sequence, ss)):
            res_code = aa_codes.get(aa, 'UNK')

            # Move along backbone
            if ss_type == 'H':  # Helix
                x += 1.5 * math.cos(phi)
                y += 1.5 * math.sin(phi)
                z += 1.5
                phi += math.radians(100)  # Helix turn
            elif ss_type == 'E':  # Sheet
                x += 3.5
                y += 0.5 * ((-1) ** i)
            else:  # Coil
                x += 3.8 * math.cos(phi)
                y += 3.8 * math.sin(phi)
                phi += math.radians(60)

            # CA atom
            plddt = 50 + (10 if ss_type != 'C' else 0)
            atom_line = f"ATOM  {i*5+1:5d}  CA  {res_code} A{i+1:4d}    {x:8.3f}{y:8.3f}{z:8.3f}  1.00{plddt:6.2f}           C"
            lines.append(atom_line)

        lines.append("END")
        return lines


