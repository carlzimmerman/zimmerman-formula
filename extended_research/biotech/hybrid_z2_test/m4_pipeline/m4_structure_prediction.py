def save_pdb(pdb_string: str, output_path: str, metadata: Dict):
    """Save PDB file with metadata header."""
    # Add prior art header
    header = f"""; ==============================================================================
; PREDICTED PROTEIN STRUCTURE - PRIOR ART PUBLICATION
; ==============================================================================
;
; LICENSE: AGPL-3.0 + OpenMTA + CC BY-SA 4.0 + Patent Dedication
;
; Name: {metadata.get('name', 'Unknown')}
; Length: {metadata.get('sequence_length', 'N/A')} residues
; Mean pLDDT: {metadata.get('mean_plddt', 'N/A')}
; Prediction Time: {metadata.get('prediction_time', 'N/A')}
;
; SHA-256: {hashlib.sha256(pdb_string.encode()).hexdigest()}
;
; This structure is PUBLIC DOMAIN for patent purposes.
;
; ==============================================================================

"""

    with open(output_path, 'w') as f:
        f.write(header)
        f.write(pdb_string)


def run_structure_pipeline():
    """Main structure prediction pipeline."""

    print("="*80)
    print("M4 STRUCTURE PREDICTION PIPELINE")
    print("="*80)
    print(f"""
This pipeline predicts 3D structures for all therapeutic sequences.

COMPUTATIONAL REQUIREMENTS:
- GPU strongly recommended (10-100x faster)
- ~2-10 minutes per sequence on GPU
- ~30-60 minutes per sequence on CPU
- Total runtime estimate: 4-12+ hours

LICENSE: AGPL-3.0 + OpenMTA + CC BY-SA 4.0 + Patent Dedication
""")

    # Create output directory
    output_dir = os.path.join(CONFIG["output_dir"])
    os.makedirs(output_dir, exist_ok=True)

    # Collect sequences
    print("\n[1/4] Collecting sequences...")
    sequences = collect_sequences()
    print(f"  Found {len(sequences)} sequences to process")

    if not sequences:
        print("  No sequences found. Run the batch processor first.")
        return

    # Initialize predictor
    print("\n[2/4] Initializing structure predictor...")

    # Try ESMFold first, fall back to simple estimator
    predictor = ESMFoldPredictor(use_gpu=CONFIG["use_gpu"])
    if not predictor.initialize():
        print("  Falling back to simple structure estimator...")
        predictor = SimpleStructureEstimator()
        predictor.initialize()

    # Process sequences
    print(f"\n[3/4] Predicting structures...")

    results = []
    failed = []
    checkpoint_file = os.path.join(output_dir, "checkpoint.json")

    # Load checkpoint if exists
    processed_ids = set()
    if os.path.exists(checkpoint_file):
        with open(checkpoint_file, 'r') as f:
            checkpoint = json.load(f)
            processed_ids = set(checkpoint.get('processed', []))
            print(f"  Resuming from checkpoint: {len(processed_ids)} already processed")

    start_time = time.time()

    for i, seq_data in enumerate(sequences):
        seq_id = f"{seq_data['source_dir']}/{seq_data['source_file']}"

        # Skip if already processed
        if seq_id in processed_ids:
            continue

        print(f"\n  [{i+1}/{len(sequences)}] {seq_data['source_file']}")
        print(f"    Length: {seq_data['length']} aa")

        # Skip very long sequences
        if seq_data['length'] > CONFIG['max_sequence_length']:
            print(f"    SKIPPED: Sequence too long (>{CONFIG['max_sequence_length']} aa)")
            failed.append({
                "file": seq_id,
                "reason": "Sequence too long"
            })
            continue

        # Predict structure
        pred_start = time.time()
        result = predictor.predict(
            seq_data['sequence'],
            name=seq_data['source_file'].replace('.fasta', '')
        )
        pred_time = time.time() - pred_start

        if result:
            print(f"    Mean pLDDT: {result['mean_plddt']}")
            print(f"    Time: {pred_time:.1f}s")

            # Save PDB
            if CONFIG["save_pdb"]:
                pdb_subdir = os.path.join(output_dir, seq_data['source_dir'])
                os.makedirs(pdb_subdir, exist_ok=True)
                pdb_path = os.path.join(pdb_subdir, seq_data['source_file'].replace('.fasta', '.pdb'))
                save_pdb(result['pdb_string'], pdb_path, result)
                print(f"    Saved: {pdb_path}")

            result['source'] = seq_id
            result['prediction_seconds'] = round(pred_time, 2)
            results.append(result)
        else:
            print(f"    FAILED: Prediction error")
            failed.append({
                "file": seq_id,
                "reason": "Prediction failed"
            })

        processed_ids.add(seq_id)

        # Save checkpoint
        if (i + 1) % CONFIG["checkpoint_interval"] == 0:
            with open(checkpoint_file, 'w') as f:
                json.dump({
                    "processed": list(processed_ids),
                    "timestamp": datetime.now().isoformat()
                }, f)

    total_time = time.time() - start_time

    # Save summary
    print(f"\n[4/4] Saving results...")

    summary = {
        "timestamp": datetime.now().isoformat(),
        "pipeline": "m4_structure_prediction",
        "license": "AGPL-3.0 + OpenMTA + CC BY-SA 4.0 + Patent Dedication",
        "total_sequences": len(sequences),
        "successful": len(results),
        "failed": len(failed),
        "total_time_seconds": round(total_time, 2),
        "avg_time_per_structure": round(total_time / max(len(results), 1), 2),
        "results": results,
        "failures": failed
    }

    summary_path = os.path.join(output_dir, "structure_summary.json")
    with open(summary_path, 'w') as f:
        json.dump(summary, f, indent=2, default=str)

    # Generate prior art manifest
    manifest = {
        "timestamp": datetime.now().isoformat(),
        "pipeline": "m4_structure_prediction",
        "license": "AGPL-3.0 + OpenMTA + CC BY-SA 4.0 + Patent Dedication",
        "structures": []
    }

    for result in results:
        if 'pdb_string' in result:
            manifest["structures"].append({
                "name": result['name'],
                "length": result['sequence_length'],
                "mean_plddt": result['mean_plddt'],
                "sha256": hashlib.sha256(result['pdb_string'].encode()).hexdigest()
            })

    manifest_path = os.path.join(output_dir, "STRUCTURE_PRIOR_ART_MANIFEST.json")
    with open(manifest_path, 'w') as f:
        json.dump(manifest, f, indent=2)

    print("\n" + "="*80)
    print("STRUCTURE PREDICTION COMPLETE")
    print("="*80)
    print(f"""
RESULTS:
  Total sequences: {len(sequences)}
  Successful predictions: {len(results)}
  Failed: {len(failed)}
  Total time: {total_time/60:.1f} minutes

OUTPUT:
  PDB files: {output_dir}/
  Summary: {summary_path}
  Prior Art Manifest: {manifest_path}

PRIOR ART NOTICE:
  All predicted structures are published under defensive license.
  SHA-256 hashes establish timestamped proof of creation.
  These structures are PUBLIC DOMAIN for patent purposes.
""")


if __name__ == "__main__":
    run_structure_pipeline()
