# =============================================================================
# ESMFOLD WRAPPER
# =============================================================================

class ESMFoldPredictor:
    """Wrapper for ESMFold structure prediction."""

    def __init__(self, use_gpu: bool = True):
        self.model = None
        self.device = None
        self.use_gpu = use_gpu
        self._initialized = False

    def initialize(self):
        """Lazy initialization of ESMFold model."""
        if self._initialized:
            return True

        try:
            import torch
            import esm

            print("Loading ESMFold model (this may take a few minutes)...")

            # Check device
            if self.use_gpu and torch.cuda.is_available():
                self.device = torch.device("cuda")
                print(f"  Using GPU: {torch.cuda.get_device_name(0)}")
            elif self.use_gpu and hasattr(torch.backends, 'mps') and torch.backends.mps.is_available():
                self.device = torch.device("mps")  # Apple Silicon
                print("  Using Apple Silicon MPS")
            else:
                self.device = torch.device("cpu")
                print("  Using CPU (this will be slow)")

            # Load ESMFold
            self.model = esm.pretrained.esmfold_v1()
            self.model = self.model.to(self.device)
            self.model.eval()

            # Set chunk size for memory efficiency
            self.model.set_chunk_size(64)

            self._initialized = True
            print("  ESMFold model loaded successfully!")
            return True

        except ImportError as e:
            print(f"  ERROR: Required packages not installed: {e}")
            print("  Install with: pip install fair-esm torch")
            return False
        except Exception as e:
            print(f"  ERROR: Failed to initialize ESMFold: {e}")
            return False

    def predict(self, sequence: str, name: str = "protein") -> Optional[Dict]:
        """
        Predict 3D structure from amino acid sequence.

        Returns dict with:
        - pdb_string: PDB format structure
        - plddt: Per-residue confidence scores
        - mean_plddt: Average confidence
        - ptm: Predicted TM-score
        """
        if not self._initialized:
            if not self.initialize():
                return None

        try:
            import torch

            # Validate sequence
            valid_aa = set("ACDEFGHIKLMNPQRSTVWY")
            clean_seq = ''.join(c for c in sequence.upper() if c in valid_aa)

            if len(clean_seq) == 0:
                print(f"    ERROR: No valid amino acids in sequence")
                return None

            if len(clean_seq) > CONFIG["max_sequence_length"]:
                print(f"    WARNING: Sequence truncated from {len(clean_seq)} to {CONFIG['max_sequence_length']} aa")
                clean_seq = clean_seq[:CONFIG["max_sequence_length"]]

            # Predict structure
            with torch.no_grad():
                output = self.model.infer_pdb(clean_seq)

            # Extract confidence scores from PDB B-factors
            plddt_scores = self._extract_plddt(output)
            mean_plddt = sum(plddt_scores) / len(plddt_scores) if plddt_scores else 0

            return {
                "name": name,
                "sequence_length": len(clean_seq),
                "pdb_string": output,
                "plddt": plddt_scores,
                "mean_plddt": round(mean_plddt, 2),
                "prediction_time": datetime.now().isoformat()
            }

        except torch.cuda.OutOfMemoryError:
            print(f"    ERROR: GPU out of memory. Try shorter sequence or CPU.")
            return None
        except Exception as e:
            print(f"    ERROR: Prediction failed: {e}")
            return None

    def _extract_plddt(self, pdb_string: str) -> List[float]:
        """Extract pLDDT scores from PDB B-factor column."""
        plddt = []
        for line in pdb_string.split('\n'):
            if line.startswith('ATOM') and ' CA ' in line:
                try:
                    # B-factor is columns 61-66 in PDB format
                    bfactor = float(line[60:66].strip())
                    plddt.append(bfactor)
                except:
                    pass
        return plddt


