def collect_sequences(base_dir: str = ".") -> List[Dict]:
    """Collect all FASTA sequences from M4 pipeline outputs."""
    sequences = []

    # Directories to scan
    dirs_to_scan = [
        "lysosomal_enzyme_bbb",
        "expired_patent_antibodies",
        "therapeutic_sequences",
        "ophthalmic_biologics",
        "genetic_capsids",
        "hematological_vectors"
    ]

    for dir_name in dirs_to_scan:
        dir_path = os.path.join(base_dir, dir_name)
        if not os.path.exists(dir_path):
            continue

        for root, dirs, files in os.walk(dir_path):
            for filename in files:
                if filename.endswith('.fasta'):
                    filepath = os.path.join(root, filename)
                    seq_data = parse_fasta(filepath)
                    if seq_data:
                        seq_data['source_dir'] = dir_name
                        seq_data['source_file'] = filename
                        sequences.append(seq_data)

    return sequences


def parse_fasta(filepath: str) -> Optional[Dict]:
    """Parse a FASTA file and extract sequence."""
    try:
        header = ""
        sequence = ""

        with open(filepath, 'r') as f:
            for line in f:
                line = line.strip()
                if line.startswith(';'):
                    continue  # Skip comments
                elif line.startswith('>'):
                    header = line[1:]
                else:
                    sequence += line

        if sequence:
            # Clean sequence
            valid_aa = set("ACDEFGHIKLMNPQRSTVWY")
            clean_seq = ''.join(c for c in sequence.upper() if c in valid_aa)

            return {
                "header": header,
                "sequence": clean_seq,
                "length": len(clean_seq),
                "filepath": filepath
            }
    except Exception as e:
        print(f"  Error parsing {filepath}: {e}")

    return None


