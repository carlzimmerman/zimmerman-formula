def generate_peptide_pdb(sequence: str, output_path: Path) -> bool:
    """
    Generate initial peptide structure from sequence.

    Uses Rosetta's PeptideStubMover or PyMOL's builder.
    For production, use AlphaFold2 predicted structure.
    """
    # This is a placeholder - in practice use:
    # 1. AlphaFold2 for peptide structure prediction
    # 2. PyMOL's build peptide feature
    # 3. Rosetta's PeptideStubMover

    pdb_template = f"""REMARK Generated by M4 FlexPepDock Pipeline
REMARK Sequence: {sequence}
REMARK Date: {datetime.now().isoformat()}
REMARK NOTE: Replace with AlphaFold2 predicted structure for production use
"""

    # Generate extended chain coordinates
    # (Simplified - real implementation would use proper backbone geometry)
    x, y, z = 0.0, 0.0, 0.0
    atom_num = 1
    res_num = 1

    lines = [pdb_template]

    for aa in sequence.upper():
        # Add backbone atoms (simplified extended chain)
        lines.append(f"ATOM  {atom_num:5d}  N   {aa_3letter(aa)} A{res_num:4d}    {x:8.3f}{y:8.3f}{z:8.3f}  1.00  0.00           N")
        atom_num += 1
        x += 1.458  # N-CA distance

        lines.append(f"ATOM  {atom_num:5d}  CA  {aa_3letter(aa)} A{res_num:4d}    {x:8.3f}{y:8.3f}{z:8.3f}  1.00  0.00           C")
        atom_num += 1
        x += 1.523  # CA-C distance

        lines.append(f"ATOM  {atom_num:5d}  C   {aa_3letter(aa)} A{res_num:4d}    {x:8.3f}{y:8.3f}{z:8.3f}  1.00  0.00           C")
        atom_num += 1

        lines.append(f"ATOM  {atom_num:5d}  O   {aa_3letter(aa)} A{res_num:4d}    {x:8.3f}{y+1.2:8.3f}{z:8.3f}  1.00  0.00           O")
        atom_num += 1
        x += 1.329  # C-N distance

        res_num += 1

    lines.append("END")

    with open(output_path, "w") as f:
        f.write("\n".join(lines))

    return True


def aa_3letter(aa: str) -> str:
    """Convert 1-letter to 3-letter amino acid code."""
    mapping = {
        'A': 'ALA', 'C': 'CYS', 'D': 'ASP', 'E': 'GLU', 'F': 'PHE',
        'G': 'GLY', 'H': 'HIS', 'I': 'ILE', 'K': 'LYS', 'L': 'LEU',
        'M': 'MET', 'N': 'ASN', 'P': 'PRO', 'Q': 'GLN', 'R': 'ARG',
        'S': 'SER', 'T': 'THR', 'V': 'VAL', 'W': 'TRP', 'Y': 'TYR'
    }
    return mapping.get(aa.upper(), 'UNK')


def generate_rosetta_script(config: FlexPepDockConfig, output_dir: Path) -> str:
    """
    Generate RosettaScripts XML for FlexPepDock.

    This defines the docking protocol in Rosetta's XML format.
    """
    xml_content = f"""<ROSETTASCRIPTS>
    <!-- M4 FlexPepDock Protocol -->
    <!-- Peptide: {config.peptide_id} -->
    <!-- Generated: {datetime.now().isoformat()} -->

    <SCOREFXNS>
        <ScoreFunction name="ref15" weights="{config.score_function}"/>
        <ScoreFunction name="soft_rep" weights="soft_rep_design"/>
        <ScoreFunction name="interchain_cen" weights="interchain_cen"/>
    </SCOREFXNS>

    <RESIDUE_SELECTORS>
        <!-- Select peptide chain (assumed chain B) -->
        <Chain name="peptide" chains="B"/>

        <!-- Select receptor (assumed chain A) -->
        <Chain name="receptor" chains="A"/>

        <!-- Interface residues -->
        <Neighborhood name="interface_receptor" selector="peptide"
                      distance="{config.receptor_flex_distance}"/>

        <!-- Combined interface -->
        <Or name="interface" selectors="peptide,interface_receptor"/>
    </RESIDUE_SELECTORS>

    <TASKOPERATIONS>
        <!-- Allow repacking at interface -->
        <OperateOnResidueSubset name="repack_interface" selector="interface">
            <RestrictToRepackingRLT/>
        </OperateOnResidueSubset>

        <!-- Prevent design (keep sequence) -->
        <RestrictToRepacking name="no_design"/>

        <!-- Include current rotamer -->
        <IncludeCurrent name="include_current"/>

        <!-- Extra chi sampling for better accuracy -->
        <ExtraRotamersGeneric name="extra_chi" ex1="1" ex2="1"/>
    </TASKOPERATIONS>

    <MOVE_MAP_FACTORIES>
        <!-- Peptide fully flexible -->
        <MoveMapFactory name="peptide_flex" bb="false" chi="false">
            <Backbone residue_selector="peptide" enable="true"/>
            <Chi residue_selector="peptide" enable="true"/>
        </MoveMapFactory>

        <!-- Receptor interface flexible -->
        <MoveMapFactory name="receptor_flex" bb="false" chi="false">
            <Backbone residue_selector="interface_receptor" enable="{str(config.receptor_bb_flex).lower()}"/>
            <Chi residue_selector="interface_receptor" enable="true"/>
        </MoveMapFactory>

        <!-- Combined flexibility -->
        <MoveMapFactory name="interface_flex" bb="false" chi="false">
            <Backbone residue_selector="interface" enable="true"/>
            <Chi residue_selector="interface" enable="true"/>
        </MoveMapFactory>
    </MOVE_MAP_FACTORIES>

    <MOVERS>
        <!-- Initial setup -->
        <SwitchChainOrder name="setup_chains" chain_order="AB"/>

        <!-- Low-resolution centroid docking -->
        <FlexPepDocking name="lowres_dock" lowres_preoptimize="true"
                        pep_fold_only="false" ppk_only="false"
                        flexpep_score_only="false"/>

        <!-- High-resolution refinement -->
        <FlexPepDocking name="highres_dock" lowres_preoptimize="false"
                        pep_refine="true" min_receptor_bb="true"/>

        <!-- Minimization -->
        <MinMover name="minimize" scorefxn="ref15"
                  movemap_factory="interface_flex" type="lbfgs_armijo_nonmonotone"
                  tolerance="0.01" max_iter="200"/>

        <!-- Side-chain repacking -->
        <PackRotamersMover name="repack" scorefxn="ref15"
                           task_operations="repack_interface,no_design,include_current,extra_chi"/>

        <!-- Interface analysis -->
        <InterfaceAnalyzerMover name="analyze" scorefxn="ref15"
                                pack_separated="true" pack_input="false"
                                compute_interface_sc="true"
                                compute_separated_sasa="true"
                                calc_hbond_sasaE="true"/>
    </MOVERS>

    <FILTERS>
        <!-- Interface score filter -->
        <ScoreType name="interface_score" scorefxn="ref15"
                   score_type="total_score" threshold="0"
                   confidence="0"/>

        <!-- Total score filter -->
        <TotalScore name="total_score" scorefxn="ref15"
                    confidence="0"/>

        <!-- SASA filter (buried surface area) -->
        <Sasa name="buried_sasa" threshold="500" confidence="0"/>
    </FILTERS>

    <PROTOCOLS>
        <!-- FlexPepDock Protocol -->
        <Add mover="setup_chains"/>

        <!-- Low-resolution phase -->
        <Add mover="lowres_dock"/>

        <!-- High-resolution refinement cycles -->
        <Add mover="highres_dock"/>
        <Add mover="repack"/>
        <Add mover="minimize"/>

        <!-- Final analysis -->
        <Add mover="analyze"/>

        <!-- Filters for output -->
        <Add filter="total_score"/>
        <Add filter="interface_score"/>
    </PROTOCOLS>

    <OUTPUT scorefxn="ref15"/>
</ROSETTASCRIPTS>
"""
    return xml_content


