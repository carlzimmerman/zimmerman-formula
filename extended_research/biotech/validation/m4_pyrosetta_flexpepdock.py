def setup_flexpep_pipeline(peptide_id: str, peptide_sequence: str,
                            receptor_pdb: str, output_base: Path) -> Dict:
    """
    Set up complete FlexPepDock pipeline for a peptide-receptor pair.
    """
    print("=" * 70)
    print("M4 PyRosetta FlexPepDock PIPELINE SETUP")
    print("=" * 70)
    print(f"Peptide: {peptide_id}")
    print(f"Sequence: {peptide_sequence}")
    print(f"Receptor: {receptor_pdb}")
    print()

    # Configuration
    config = FlexPepDockConfig(
        peptide_sequence=peptide_sequence,
        peptide_id=peptide_id,
        receptor_pdb=receptor_pdb
    )

    # Create output directory
    output_dir = output_base / peptide_id
    output_dir.mkdir(parents=True, exist_ok=True)

    models_dir = output_dir / "models"
    models_dir.mkdir(exist_ok=True)

    # Generate files
    print("Generating pipeline files...")

    # 1. RosettaScripts XML
    xml_content = generate_rosetta_script(config, output_dir)
    with open(output_dir / "flexpep_protocol.xml", "w") as f:
        f.write(xml_content)
    print("  - flexpep_protocol.xml (RosettaScripts)")

    # 2. PyRosetta Python script
    pyrosetta_script = generate_pyrosetta_script(config, output_dir)
    with open(output_dir / "flexpep_pyrosetta.py", "w") as f:
        f.write(pyrosetta_script)
    print("  - flexpep_pyrosetta.py (PyRosetta)")

    # 3. Bash wrapper
    bash_script = generate_bash_wrapper(config, output_dir)
    with open(output_dir / "run_flexpep.sh", "w") as f:
        f.write(bash_script)
    (output_dir / "run_flexpep.sh").chmod(0o755)
    print("  - run_flexpep.sh (wrapper script)")

    # 4. Generate placeholder peptide PDB
    generate_peptide_pdb(peptide_sequence, output_dir / "peptide.pdb")
    print("  - peptide.pdb (placeholder - replace with AlphaFold2)")

    # 5. Configuration JSON
    config_dict = {
        "peptide_id": peptide_id,
        "peptide_sequence": peptide_sequence,
        "receptor_pdb": receptor_pdb,
        "config": asdict(config),
        "output_directory": str(output_dir),
        "generated": datetime.now().isoformat(),
        "files": [
            "flexpep_protocol.xml",
            "flexpep_pyrosetta.py",
            "run_flexpep.sh",
            "peptide.pdb"
        ]
    }

    with open(output_dir / "flexpep_config.json", "w") as f:
        json.dump(config_dict, f, indent=2)

    print(f"\nConfiguration saved: {output_dir / 'flexpep_config.json'}")

    return config_dict


