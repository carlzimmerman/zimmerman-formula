def compare_to_z2(homology_result):
    """
    Compare observed death radii to Z² prediction.

    THIS IS THE CRITICAL TEST:
    - If death radii cluster around 9.14 Å, Z² may be real
    - If they cluster elsewhere, Z² is not supported
    """
    print("\n" + "=" * 80)
