def extract_ca_coordinates(pdb_file):
    """Extract C-alpha coordinates from PDB file."""
    print("\nSTEP 2: Extracting C-alpha coordinates...")

    coords = []
    residue_ids = []

    with open(pdb_file, 'r') as f:
        for line in f:
            if line.startswith('ATOM') and ' CA ' in line:
                try:
                    x = float(line[30:38].strip())
                    y = float(line[38:46].strip())
                    z = float(line[46:54].strip())
                    res_num = int(line[22:26].strip())

                    coords.append([x, y, z])
                    residue_ids.append(res_num)
                except ValueError:
                    continue

    coords = np.array(coords)
    print(f"  Extracted {len(coords)} C-alpha atoms")
    print(f"  Residue range: {min(residue_ids)} to {max(residue_ids)}")

    return coords, residue_ids


