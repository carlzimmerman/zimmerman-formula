    print("=" * 80)

    if homology_result['n_loops'] == 0:
        print("  No loops to analyze - cannot compare")
        return None

    death_radii = np.array(homology_result['death_radii'])
    mean_death = homology_result['mean_death']
    median_death = homology_result['median_death']

    print(f"\n  Z² prediction: r_natural = {R_NATURAL:.4f} Å")
    print(f"  Observed mean death radius: {mean_death:.4f} Å")
    print(f"  Observed median death radius: {median_death:.4f} Å")

    # Check how many death radii are within 10% of Z² or multiples
    z2_multiples = [R_NATURAL * n for n in range(1, 10)]
    tolerance = 0.10  # 10%

    matches = 0
    for death in death_radii:
        for mult in z2_multiples:
            if abs(death - mult) / mult < tolerance:
                matches += 1
                break

    match_rate = matches / len(death_radii)

    print(f"\n  Death radii within 10% of Z² multiples: {matches}/{len(death_radii)} ({match_rate:.1%})")

    # Distribution analysis
    print("\n  Death radius distribution:")
    bins = [0, 5, 7, 8, 9, 10, 11, 12, 15, 20, 30, 50]
    hist, _ = np.histogram(death_radii, bins=bins)
    for i in range(len(hist)):
        bar = '█' * (hist[i] * 2)
        print(f"    {bins[i]:4.0f}-{bins[i+1]:4.0f} Å: {hist[i]:3d} {bar}")

    # Verdict
    print("\n" + "=" * 80)
    print("  VERDICT:")
    print("=" * 80)

    # Calculate deviation from Z²
    mean_deviation = abs(mean_death - R_NATURAL) / R_NATURAL * 100
    median_deviation = abs(median_death - R_NATURAL) / R_NATURAL * 100

    print(f"\n  Mean deviation from Z²: {mean_deviation:.1f}%")
    print(f"  Median deviation from Z²: {median_deviation:.1f}%")

    if mean_deviation < 10 and match_rate > 0.3:
        verdict = "SUPPORTIVE - Death radii cluster near Z² prediction"
        support_level = "MODERATE"
    elif mean_deviation < 20 and match_rate > 0.2:
        verdict = "WEAK SUPPORT - Some alignment with Z², but not strong"
        support_level = "WEAK"
    elif mean_deviation < 30:
        verdict = "INCONCLUSIVE - Death radii don't clearly support or refute Z²"
        support_level = "NONE"
    else:
        verdict = "NOT SUPPORTED - Death radii do not match Z² prediction"
        support_level = "NEGATIVE"

    print(f"\n  {verdict}")
    print(f"  Support level: {support_level}")

    # Critical note
    print("\n" + "-" * 80)
    print("  CRITICAL NOTE:")
    print("  This is ONE protein. Proper validation requires:")
    print("  1. Testing on 100+ diverse proteins")
    print("  2. Statistical comparison to null hypothesis")
    print("  3. Checking if other constants (8 Å, 10 Å) fit equally well")
    print("-" * 80)

    return {
        'mean_death': mean_death,
        'median_death': median_death,
        'z2_prediction': R_NATURAL,
        'mean_deviation_percent': mean_deviation,
        'median_deviation_percent': median_deviation,
        'match_rate': match_rate,
        'verdict': verdict,
        'support_level': support_level,
    }


