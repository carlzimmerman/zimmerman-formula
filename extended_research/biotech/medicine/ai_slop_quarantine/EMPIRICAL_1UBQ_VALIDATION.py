def compute_fallback_homology(dist_matrix):
    """
    Fallback persistent homology without ripser.

    This is a SIMPLIFIED approximation using Vietoris-Rips filtration.
    NOT as accurate as ripser, but gives rough estimates.
    """
    n = len(dist_matrix)

    # Get all unique distances (edge lengths)
    edges = []
    for i in range(n):
        for j in range(i+1, n):
            edges.append((dist_matrix[i, j], i, j))

    edges.sort(key=lambda x: x[0])

    # Track connected components (H0)
    # Using simple union-find
    parent = list(range(n))

    def find(x):
        if parent[x] != x:
            parent[x] = find(parent[x])
        return parent[x]

    def union(x, y):
        px, py = find(x), find(y)
        if px != py:
            parent[px] = py
            return True
        return False

    h0_births = [0.0] * n
    h0_deaths = []

    # Track loops (H1) - when edges complete triangles
    h1_features = []

    # Adjacency tracking
    adjacency = [set() for _ in range(n)]

    for dist, i, j in edges:
        # Check if this edge closes a triangle (creates a loop)
        common_neighbors = adjacency[i] & adjacency[j]
        if common_neighbors:
            # This edge completes at least one triangle
            # Approximate: record this as loop death
            for k in common_neighbors:
                # Find when the triangle edges formed
                birth = max(dist_matrix[i, k], dist_matrix[j, k])
                if birth < dist:  # Valid cycle
                    h1_features.append([birth, dist])

        # Union the components
        if union(i, j):
            h0_deaths.append(dist)

        # Update adjacency
        adjacency[i].add(j)
        adjacency[j].add(i)

    # Format as persistence diagrams
    h0 = np.array([[0.0, d] for d in h0_deaths] + [[0.0, np.inf]] if h0_deaths else [[0.0, np.inf]])
    h1 = np.array(h1_features) if h1_features else np.array([]).reshape(0, 2)
    h2 = np.array([]).reshape(0, 2)  # H2 not computed in fallback

    return h0, h1, h2


