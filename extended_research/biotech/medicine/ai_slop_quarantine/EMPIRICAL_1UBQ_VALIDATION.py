    if homology_result['n_loops'] == 0:
        print("  No data to analyze")
        return None

    death_radii = np.array(homology_result['death_radii'])
    mean_death = homology_result['mean_death']

    # Test multiple candidate constants
    candidates = [
        ('Z² = 32π/3', R_NATURAL),       # Our claim: 9.1428 Å
        ('8.0 Å', 8.0),                   # Alternative
        ('8.5 Å', 8.5),                   # Alternative
        ('9.0 Å', 9.0),                   # Close to Z²
        ('9.5 Å', 9.5),                   # Alternative
        ('10.0 Å', 10.0),                 # Alternative (helix diameter)
        ('10.5 Å', 10.5),                 # Alternative
        ('Observed mean', mean_death),    # Best fit by definition
    ]

    print("\n  Comparing candidate constants to observed death radii:\n")

    results = []
    for name, value in candidates:
        # Calculate match rate with 10% tolerance
        multiples = [value * n for n in range(1, 10)]
        matches = 0
        for death in death_radii:
            for mult in multiples:
                if abs(death - mult) / mult < 0.10:
                    matches += 1
                    break
        match_rate = matches / len(death_radii)

        # Mean deviation
        deviation = abs(mean_death - value) / value * 100

        results.append({
            'name': name,
            'value': value,
            'match_rate': match_rate,
            'deviation': deviation,
        })

        bar = '█' * int(match_rate * 40)
        print(f"    {name:20s} ({value:5.2f} Å): {match_rate:5.1%} match, {deviation:5.1f}% dev  {bar}")

    # Find best match
    best = max(results, key=lambda x: x['match_rate'])

