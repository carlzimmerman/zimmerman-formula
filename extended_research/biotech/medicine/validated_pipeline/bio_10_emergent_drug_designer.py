@dataclass
class BindingPocket:
    """Represents a binding pocket in a target protein."""
    center: np.ndarray
    residue_positions: np.ndarray
    residue_types: List[str]
    pocket_volume: float
    pocket_radius: float
    surface_area: float


@dataclass
class GeneratedPeptide:
    """A peptide generated by the geometric algorithm."""
    sequence: str
    positions: np.ndarray
    mean_contact_distance: float
    ideal_distance_deviation: float
    binding_score: float
    z2_fitness: float
    rationale: str


def load_scaling_law(scaling_law_path: Path) -> Dict:
    """
    Load the Thermodynamic Expansion Multiplier from bio_09 output.
    """
    if not scaling_law_path.exists():
        warnings.warn(f"Scaling law not found at {scaling_law_path}. Using default multiplier.")
        # Default based on our observations (~4% expansion)
        return {
            'thermodynamic_expansion_multiplier': {
                'value': 1.04,
                'std': 0.02,
            },
            'vacuum_constants': {
                'Z2_distance_angstrom': Z2_DISTANCE,
            }
        }

    with open(scaling_law_path) as f:
        return json.load(f)


def parse_pdb_structure(pdb_path: Path) -> Tuple[np.ndarray, List[str], List[str]]:
    """
    Parse a PDB file and extract atom coordinates.

    Returns:
        positions: Nx3 array of atom positions
        residue_names: List of residue names for each atom
        atom_names: List of atom names
    """
    positions = []
    residue_names = []
    atom_names = []

    with open(pdb_path) as f:
        for line in f:
            if line.startswith(('ATOM', 'HETATM')):
                x = float(line[30:38])
                y = float(line[38:46])
                z = float(line[46:54])
                positions.append([x, y, z])
                residue_names.append(line[17:20].strip())
                atom_names.append(line[12:16].strip())

    return np.array(positions), residue_names, atom_names


def identify_binding_pocket(
    positions: np.ndarray,
    residue_names: List[str],
    pocket_center: Optional[np.ndarray] = None,
    pocket_radius: float = 12.0
) -> BindingPocket:
    """
    Identify a binding pocket in the protein structure.

    If pocket_center is not provided, uses the geometric center of the protein.
    """
    if pocket_center is None:
        # Use geometric center as default
        pocket_center = np.mean(positions, axis=0)

    # Find atoms within pocket radius
    distances = np.linalg.norm(positions - pocket_center, axis=1)
    pocket_mask = distances < pocket_radius

    pocket_positions = positions[pocket_mask]
    pocket_residues = [r for i, r in enumerate(residue_names) if pocket_mask[i]]

    # Estimate pocket volume using convex hull
    if len(pocket_positions) >= 4:
        try:
            hull = ConvexHull(pocket_positions)
            volume = hull.volume
            surface_area = hull.area
        except Exception:
            volume = (4/3) * np.pi * pocket_radius**3
            surface_area = 4 * np.pi * pocket_radius**2
    else:
        volume = (4/3) * np.pi * pocket_radius**3
        surface_area = 4 * np.pi * pocket_radius**2

    return BindingPocket(
        center=pocket_center,
        residue_positions=pocket_positions,
        residue_types=pocket_residues,
        pocket_volume=volume,
        pocket_radius=pocket_radius,
        surface_area=surface_area
    )


def select_amino_acid_by_geometry(
    target_distance: float,
    local_pocket_radius: float,
    neighbor_charges: List[float],
    temperature: float = 310.0,
    tolerance: float = 0.3
) -> Tuple[str, float]:
    """
    Select the optimal amino acid to maintain the ideal binding distance.

    The algorithm selects based on:
    1. VdW radius that achieves target distance from pocket wall
    2. Charge complementarity with neighboring residues
    3. Hydrophobicity matching

    Returns: (amino_acid_code, fitness_score)
    """
    best_aa = 'A'
    best_score = float('-inf')

    # Desired VdW radius to achieve target distance
    # If pocket wall is at local_pocket_radius, we want:
    # local_pocket_radius - AA_radius ≈ target_distance
    desired_radius = local_pocket_radius - target_distance

    for code, props in AMINO_ACIDS.items():
        # Distance fitness: how close is this AA's effective radius to desired?
        radius_diff = abs(props['vdw_radius'] - desired_radius)
        distance_fitness = np.exp(-radius_diff**2 / (2 * tolerance**2))

        # Charge complementarity
        mean_neighbor_charge = np.mean(neighbor_charges) if neighbor_charges else 0
        charge_fitness = 1.0 - 0.3 * abs(props['charge'] + mean_neighbor_charge)
        charge_fitness = max(0, charge_fitness)

        # Size-to-volume ratio (prefer efficient packing)
        volume_efficiency = 1.0 / (1.0 + abs(props['vol'] - Z2_VOLUME * 3) / 100)

        # Combined score
        score = 0.5 * distance_fitness + 0.3 * charge_fitness + 0.2 * volume_efficiency

        if score > best_score:
            best_score = score
            best_aa = code

    return best_aa, best_score


def spatial_growth_algorithm(
    pocket: BindingPocket,
    ideal_distance: float,
    peptide_length: int = 8,
    n_candidates: int = 10
) -> List[GeneratedPeptide]:
    """
    Grow peptides inside the binding pocket using geometric constraints.

    The algorithm:
    1. Places the first residue near the pocket center
    2. Each subsequent residue is placed ~3.8 Å along the backbone
    3. Side chain selection maintains ideal_distance from pocket walls
    4. Multiple candidates are generated with different starting orientations

    This is the core Z² geometric drug design algorithm.
    """
    generated = []

    # CA-CA distance for peptide backbone
    CA_CA_DISTANCE = 3.8  # Å

    for candidate_idx in range(n_candidates):
        # Generate random starting position near pocket center
        start_offset = np.random.randn(3) * 2.0
        current_pos = pocket.center + start_offset

        # Random starting direction
        direction = np.random.randn(3)
        direction /= np.linalg.norm(direction)

        positions = [current_pos.copy()]
        sequence = []
        contact_distances = []

        for res_idx in range(peptide_length):
            # Calculate distance to nearest pocket wall atom
            if len(pocket.residue_positions) > 0:
                dists_to_pocket = np.linalg.norm(
                    pocket.residue_positions - current_pos, axis=1
                )
                local_pocket_radius = np.min(dists_to_pocket)

                # Get charges of nearby residues
                nearby_mask = dists_to_pocket < 5.0
                neighbor_charges = []
                for i, (res, is_near) in enumerate(zip(pocket.residue_types, nearby_mask)):
                    if is_near and res in AMINO_ACIDS:
                        neighbor_charges.append(AMINO_ACIDS.get(res, {}).get('charge', 0))
            else:
                local_pocket_radius = pocket.pocket_radius / 2
                neighbor_charges = []

            # Select optimal amino acid for this position
            aa_code, fitness = select_amino_acid_by_geometry(
                ideal_distance,
                local_pocket_radius,
                neighbor_charges
            )
            sequence.append(aa_code)
            contact_distances.append(local_pocket_radius)

            # Move to next position along backbone
            # Add some curve to follow the pocket shape
            if len(pocket.residue_positions) > 0:
                # Gradient: move toward pocket center
                to_center = pocket.center - current_pos
                to_center /= (np.linalg.norm(to_center) + 1e-6)

                # Combine with random walk
                direction = 0.7 * direction + 0.3 * to_center
                direction /= np.linalg.norm(direction)

            current_pos = current_pos + direction * CA_CA_DISTANCE
            positions.append(current_pos.copy())

            # Slight random perturbation to direction for next step
            direction += np.random.randn(3) * 0.2
            direction /= np.linalg.norm(direction)

        # Calculate peptide quality metrics
        positions_arr = np.array(positions[:-1])  # Exclude last (it's for next residue)
        mean_contact = np.mean(contact_distances) if contact_distances else 0
        deviation = abs(mean_contact - ideal_distance)

        # Z² fitness: how close are mean contacts to ideal?
        z2_fitness = np.exp(-(deviation**2) / (2 * 1.0**2))

        # Binding score heuristic
        binding_score = z2_fitness * 10  # Scale to approximate kcal/mol

        # Adjust for favorable interactions
        net_charge = sum(AMINO_ACIDS[aa]['charge'] for aa in sequence)
        if abs(net_charge) <= 2:  # Moderate charge is better
            binding_score += 2

        peptide = GeneratedPeptide(
            sequence=''.join(sequence),
            positions=positions_arr,
            mean_contact_distance=mean_contact,
            ideal_distance_deviation=deviation,
            binding_score=-binding_score,  # Negative is better (like ΔG)
            z2_fitness=z2_fitness,
            rationale=f"Generated via Z² geometric growth. Target: {ideal_distance:.2f} Å, Achieved: {mean_contact:.2f} Å"
        )
        generated.append(peptide)

    # Sort by binding score (most negative = best)
    generated.sort(key=lambda p: p.binding_score)

    return generated


def design_peptides_for_target(
    pdb_path: Path,
    scaling_law: Dict,
    peptide_length: int = 8,
    n_designs: int = 20,
    pocket_center: Optional[np.ndarray] = None
) -> Dict:
    """
    Main design function: generate optimized peptides for a protein target.
    """
    # Load the expansion multiplier
    multiplier_data = scaling_law.get('thermodynamic_expansion_multiplier', {})
    multiplier = multiplier_data.get('value', 1.04)
    multiplier_std = multiplier_data.get('std', 0.02)

    # Calculate ideal binding distance
    vacuum_distance = scaling_law.get('vacuum_constants', {}).get('Z2_distance_angstrom', Z2_DISTANCE)
    ideal_distance = vacuum_distance * multiplier
    distance_tolerance = vacuum_distance * multiplier_std

    print(f"\n🎯 IDEAL BINDING DISTANCE: {ideal_distance:.2f} ± {distance_tolerance:.2f} Å")
    print(f"   (√Z² = {vacuum_distance:.2f} Å × {multiplier:.4f} multiplier)")

    # Parse the target structure
    print(f"\n📂 Loading target structure: {pdb_path.name}")
    positions, residue_names, atom_names = parse_pdb_structure(pdb_path)
    print(f"   Loaded {len(positions)} atoms")

    # Identify binding pocket
    pocket = identify_binding_pocket(positions, residue_names, pocket_center)
    print(f"\n🎯 Binding pocket identified:")
    print(f"   Center: ({pocket.center[0]:.1f}, {pocket.center[1]:.1f}, {pocket.center[2]:.1f})")
    print(f"   Volume: {pocket.pocket_volume:.1f} Å³")
    print(f"   Surface area: {pocket.surface_area:.1f} Å²")
    print(f"   Residues in pocket: {len(pocket.residue_types)}")

    # Generate peptides
    print(f"\n🧬 Generating {n_designs} peptide candidates (length {peptide_length})...")
    peptides = spatial_growth_algorithm(
        pocket,
        ideal_distance,
        peptide_length,
        n_candidates=n_designs
    )

    # Report top candidates
    print("\n" + "=" * 70)
    print("TOP GEOMETRIC PEPTIDE DESIGNS")
    print("=" * 70)

    results = {
        'target': pdb_path.name,
        'ideal_binding_distance': ideal_distance,
        'expansion_multiplier': multiplier,
        'pocket': {
            'center': pocket.center.tolist(),
            'volume': pocket.pocket_volume,
            'surface_area': pocket.surface_area,
        },
        'designs': []
    }

    for i, peptide in enumerate(peptides[:10]):  # Top 10
        print(f"\n  #{i+1}: {peptide.sequence}")
        print(f"      Predicted ΔG: {peptide.binding_score:.1f} kcal/mol")
        print(f"      Mean contact distance: {peptide.mean_contact_distance:.2f} Å")
        print(f"      Deviation from ideal: {peptide.ideal_distance_deviation:.2f} Å")
        print(f"      Z² fitness: {peptide.z2_fitness:.3f}")

        results['designs'].append({
            'rank': i + 1,
            'sequence': peptide.sequence,
            'predicted_dG_kcal_mol': peptide.binding_score,
            'mean_contact_distance': peptide.mean_contact_distance,
            'ideal_distance_deviation': peptide.ideal_distance_deviation,
            'z2_fitness': peptide.z2_fitness,
            'rationale': peptide.rationale,
            'positions': peptide.positions.tolist() if peptide.positions is not None else None
        })

    return results


